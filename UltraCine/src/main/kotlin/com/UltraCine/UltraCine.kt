                val source = button.attr("data-source")
                if (source.isNotBlank() && loadExtractor(source, finalUrl, subtitleCallback, callback)) {
                    return true
                }
            }
            
            false
        } catch (e: Exception) {
            println("üí• ERRO NO loadLinks: ${e.message}")
            e.printStackTrace()
            false
        }
